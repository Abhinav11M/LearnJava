package com.learn.testproject;

public class Calc {
	
	public int add(int a, int b) {
		int addt = (a+b);
		return addt;
	}

	public static int sub(int a, int b) {
		int subt = (a-b);
		return subt;
	}
	
	public static int mul(int a , int b) {
		return (a*b);
	}
	
	public static int div(int a, int b) {
		return (a/b);
	}
}
